    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
       if (selecttedStudent == null || selecttedCourse == null || krs == null) {
            JOptionPane.showMessageDialog(this, "Lengkapi pilihan data dan hitung nilai terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
       
        selecttedStudent.addKRS(krs); 
        String namaDosen = (selecttedLecturer != null) ? selecttedLecturer.toString() : "-";
        
       
        Object[] rowData = {
            selecttedStudent.getNim(),
            selecttedStudent.getName(), 
            krs.getCourse().getCourseName(),
            krs.getCourse().getSKS(),
            krs.getGrade(),
            namaDosen
        };
        DefaultTableModel model = (DefaultTableModel) jTableNilai.getModel(); 
        model.addRow(rowData); 
        
     
        String code = selecttedCourse.getCode();
        String nim = selecttedStudent.getNim();
        String nidn = (selecttedLecturer != null) ? selecttedLecturer.getNidn() : "";
        int sem = Integer.parseInt(jComboBoxSM.getSelectedItem().toString());
        
        int uas = Integer.parseInt(jTextFieldNilaiUAS.getText().trim());
        int uts = Integer.parseInt(jTextFieldNilaiUTS.getText().trim());
        int sikap = Integer.parseInt(jTextFieldNilaiSikap.getText().trim());
        double scoreRataRata = (uas + uts + sikap) / 3.0;
        
        krs.setNim(nim);
        krs.setNidn(nidn);
        krs.setSemester(sem);
        
        
        int status = krsController.create(krs);
        if (status == 1) {
            JOptionPane.showMessageDialog(this, "Data KRS & Nilai (" + krs.getGrade() + ") Berhasil Disimpan ke MySQL!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan nilai ke database!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

