    private void loadDataDariDatabase() {
        try {
          
            listMahasiswa.clear();
            listMK.clear();
            listDosen.clear();
            
            
            listMahasiswa.addAll(studentController.getStudent());
            listMK.addAll(courseController.getCourse());
            listDosen.addAll(lecturerController.getLecturer());
            
           
            comboModelLecturer.removeAllElements();
            for (Lecturer lecturer : listDosen) {
                comboModelLecturer.addElement(lecturer);
            }

           
            comboModelMhs.removeAllElements();
            for (Student student : listMahasiswa) { 
                comboModelMhs.addElement(student); 
            }

            
            comboModelCourse.removeAllElements();
            for (Course course : listMK) { 
                comboModelCourse.addElement(course); 
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data otomatis: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

