    private void jComboBoxMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxMKActionPerformed
        this.selecttedCourse = (Course) jComboBoxMK.getSelectedItem(); 
        if(selecttedCourse != null) { 
            
            jLabelKodeSKS.setText(selecttedCourse.getCode()+" | SKS: "+Integer.toString(selecttedCourse.getSKS())); 
            
            
            String semesterTarget = String.valueOf(selecttedCourse.getSemester());
            jComboBoxSM.setSelectedItem(semesterTarget);
            jComboBoxSM.setEnabled(false); 
            
            
            boolean dosenDitemukan = false;
            
           
            for (int i = 0; i < jComboBoxDosen.getItemCount(); i++) {
                Lecturer dosen = jComboBoxDosen.getItemAt(i);
                
                
                if (dosen != null && dosen.toString().equalsIgnoreCase(selecttedCourse.getCourseName()) 
                    || (dosen.getExpertise() != null && dosen.getExpertise().equalsIgnoreCase(selecttedCourse.getCourseName()))) {
                    
                    jComboBoxDosen.setSelectedIndex(i);
                    this.selecttedLecturer = dosen;
                    dosenDitemukan = true;
                    break;
                }
            }
            
            
            if (!dosenDitemukan) {
                jComboBoxDosen.setSelectedIndex(-1);
                this.selecttedLecturer = null;
            }
            jComboBoxDosen.setEnabled(false); 
            
            
            setKRS(); 
            if (this.krs != null) {
                jLabelHuruf.setText(this.krs.getGrade()); 
            } else {
                jLabelHuruf.setText("-");
            }
        }
    }//GEN-LAST:event_jComboBoxMKActionPerformed

